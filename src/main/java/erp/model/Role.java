package erp.model;

public class Role {
    private int id;
    private String name;

    // Getter e Setter para id
    public int getId() {
        return id;
    }
    
    public void setId(int id) {
        this.id = id;
    }

    // Getter e Setter para name
    public String getName() {
        return name;
    }
    
    public void setName(String name) {
        this.name = name;
    }
}
